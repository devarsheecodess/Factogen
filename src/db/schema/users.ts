import mongoose from "mongoose";

const userSchena = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const users =
  mongoose.models.users || mongoose.model("users", userSchena);
